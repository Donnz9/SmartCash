    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        String cari = txt_cari.getText();
        DefaultTableModel searc = new DefaultTableModel();
        searc.setColumnIdentifiers(new Object[]{"ID", "Nama", "No Telp", "Jenis Kelamin"});     // Menetapkan kolom untuk model tabel hasil pencarian
        for (int i = 0; i < tbl.getRowCount(); i++) {       // Loop melalui setiap baris dalam tabel asli
            String id = tbl.getValueAt(i, 0).toString();        // Mendapatkan nilai dari setiap kolom di baris saat ini
            String nama = tbl.getValueAt(i, 1).toString();
            String  noTelp= tbl.getValueAt(i, 2).toString();
            String jenisKelamin = tbl.getValueAt(i, 3).toString();
            
            // Memeriksa apakah teks yang dicari ada di salah satu kolom
            if (id.contains(cari) || nama.contains(cari) || noTelp.contains(cari) || jenisKelamin.contains(cari)) {
                searc.addRow(new Object[]{id, nama, noTelp, jenisKelamin});     // Jika ya, tambahkan baris tersebut ke model tabel hasil pencarian
            }
        }
        table.setModel(searc);
    }//GEN-LAST:event_btn_cariActionPerformed

