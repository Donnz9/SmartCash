    public void bubbleSort(DefaultTableModel tbl, int indexSelected) {
        int rowCount = tbl.getRowCount();
        for (int i = 0; i < rowCount - 1; i++) {    // Looping untuk setiap pasangan baris dalam tabel
            for (int j = 0; j < rowCount - i - 1; j++) {
                String currentString = (String) tbl.getValueAt(j, indexSelected);   // Mengambil nilai dari kolom yang dipilih pada baris saat ini dan baris berikutnya
                String nextString = (String) tbl.getValueAt(j + 1, indexSelected);
                // For jenis kelamin column, convert to integer for comparison
                if (indexSelected == 3) {   // Jika kolom yang dipilih adalah kolom jenis kelamin
                    int currentGender = currentString.equals("Laki-laki") ? 1 : 0;  // Konversi string menjadi bilangan bulat (0 atau 1) untuk perbandingan
                    int nextGender = nextString.equals("Laki-laki") ? 1 : 0;
                    if (currentGender > nextGender) {   // Jika jenis kelamin pada baris saat ini lebih besar dari jenis kelamin pada baris berikutnya
                        for (int k = 0; k < tbl.getColumnCount(); k++) {    // tukar baris
                            Object temp = tbl.getValueAt(j, k);
                            tbl.setValueAt(tbl.getValueAt(j + 1, k), j, k);
                            tbl.setValueAt(temp, j + 1, k);
                        }
                    }
                } else {    // Jika kolom yang dipilih bukan jenis kelamin, bandingkan nilai string secara langsung
                    if (currentString.compareTo(nextString) > 0) {
                        for (int k = 0; k < tbl.getColumnCount(); k++) {    // tukar baris
                            Object temp = tbl.getValueAt(j, k);
                            tbl.setValueAt(tbl.getValueAt(j + 1, k), j, k);
                            tbl.setValueAt(temp, j + 1, k);
                        }
                    }
                }
            }
        }
    }

