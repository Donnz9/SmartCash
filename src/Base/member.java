    public void datatable() {
        tbl = new DefaultTableModel();
        tbl.addColumn("ID");
        tbl.addColumn("Nama");
        tbl.addColumn("No Telp");
        tbl.addColumn("Jenis Kelamin");
        try {
            Statement statement = (Statement) konek.getConnection().createStatement();
            String query = "select * from pelanggan;";
            ResultSet res = statement.executeQuery(query);
            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("id_pelanggan"),
                    res.getString("nama_pelanggan"),
                    res.getString("no_telp"),
                    res.getString("jenis_kelamin"),});
            }
            table.setModel(tbl);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Salah");
            e.printStackTrace();
        }
    }

    private String generateMemberID() {
        try {
            Statement statement = konek.getConnection().createStatement();
            ResultSet rs = statement.executeQuery("SELECT MAX(CAST(SUBSTRING(id_pelanggan, 2) AS SIGNED)) AS max_id FROM pelanggan;");
            if (rs.next()) {
                int maxId = rs.getInt("max_id");
                return String.format("M%03d", maxId + 1);
            } else {
                return "M001"; // Jika tidak ada produk di database
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            return null;
        }
    }

